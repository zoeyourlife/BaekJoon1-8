#include <stdio.h>
int main()
{
	int i, j, n;
	scanf("%d", &n);
	for (i = 0;i < n;i++) {  // 1. i가 1씩 증가할 때 마다 개행
		for (j = 0;j < n - i - 1;j++)  // 2. n-i-1=" ", 즉 띄어쓰기를 해주기 위함. 
			printf(" "); //  3." "을 출력
		for (j = 0;j <= i;j++) //  4. j가 i보다 크지 않을 때까지 증가시켜주고
			printf("*"); //  5. *출력
		printf("\n"); //  6. 개행
	}
	return 0; // ex) n=3, 1-(2-3-2-3-4-5-6)-1-(2-3-4-5-4-5-6)-1-(4-5-4-5-4-5-6).
}